package spawn

import "sync"

// historyCap — максимальный объём сохраняемого хвоста вывода терминала (байты).
// Хранится последний фрагмент вывода pty, достаточный, чтобы при переподключении
// или перезагрузке страницы клиент увидел актуальное содержимое экрана и недавнюю
// историю, не воспроизводя весь вывод с начала сессии.
const historyCap = 256 * 1024

// history — потокобезопасный кольцевой буфер последнего вывода терминала.
//
// Вывод pty живёт только в xterm на стороне клиента и теряется при перезагрузке
// страницы; новое WebSocket-подключение видит лишь последующий вывод. Буфер
// сохраняет хвост вывода на стороне сервера и отдаётся новому подключению перед
// стримом живых данных (см. transport/termws), благодаря чему терминал
// восстанавливается при reconnect.
type history struct {
	mu  sync.Mutex
	buf []byte // содержимое в хронологическом порядке, длина ≤ historyCap
}

// append добавляет фрагмент вывода, удерживая суммарный размер в пределах historyCap
// (старшие байты отбрасываются).
func (h *history) append(p []byte) {
	h.mu.Lock()
	defer h.mu.Unlock()

	if len(p) >= historyCap {
		// Новый фрагмент сам по себе больше ёмкости — оставляем только его хвост.
		h.buf = append(h.buf[:0], p[len(p)-historyCap:]...)
		return
	}
	if len(h.buf)+len(p) > historyCap {
		// Освобождаем место под новый фрагмент, отбрасывая самые старые байты.
		drop := len(h.buf) + len(p) - historyCap
		h.buf = h.buf[drop:]
	}
	h.buf = append(h.buf, p...)
}

// snapshot возвращает копию накопленного вывода для отправки новому подключению.
func (h *history) snapshot() []byte {
	h.mu.Lock()
	defer h.mu.Unlock()
	out := make([]byte, len(h.buf))
	copy(out, h.buf)
	return out
}
